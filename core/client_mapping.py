def map_client(full_name: str) -> str:
    """ממפה שם מלא מהחשבונאות לשם בצינור. Fallback: מחזיר את השם המלא."""
    mapped = CLIENT_MAPPING.get(full_name)
    if mapped:
        return mapped
    # None = לא ממופה → fallback לשם המלא (לא יימצא match בצינור)
    return full_name


# Pipeline-level canonicalisation: when source files (Andromeda/costs.pdf)
# write the same company name in two different ways within the pipeline itself,
# this map collapses them to a single canonical form. Apply to the ``client``
# column after loading the parquet (see ``core.preprocessor``).
PIPELINE_CANONICAL_NAMES: dict[str, str] = {
    # Wolfman — costs.pdf for site 'ולפמן אדירים' writes 'בעמ' (no quotes),
    # but Andromeda Excel and תקן.xlsx use 'בע"מ' (with quotes).
    'ולפמן תעשיות בעמ': 'ולפמן תעשיות בע"מ',
}


def canonicalize_pipeline_name(name: str) -> str:
    """Return the canonical pipeline-level spelling of a client name.

    Used to merge variants like ``ולפמן תעשיות בעמ`` and
    ``ולפמן תעשיות בע"מ`` into one. Idempotent — names without an entry
    in :data:`PIPELINE_CANONICAL_NAMES` are returned unchanged.
    """
    if not isinstance(name, str):
        return name
    return PIPELINE_CANONICAL_NAMES.get(name.strip(), name)
